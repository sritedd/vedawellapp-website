/**
 * PDF Export Utility for HomeOwner Guardian
 * Generates professional PDF reports for various Guardian features
 */

export interface PDFReportData {
    projectName: string;
    builderName: string;
    contractValue: number;
    generatedDate: string;
}

export interface DefectReportItem {
    title: string;
    location: string;
    severity: string;
    status: string;
    reportedDate: string;
    description: string;
}

export interface VariationReportItem {
    title: string;
    description: string;
    amount: number;
    status: string;
    date: string;
}

export interface PaymentMilestone {
    stage: string;
    percentage: number;
    amount: number;
    status: string;
    dueDate: string;
}

/**
 * Format currency
 */
function formatCurrency(amount: number): string {
    return new Intl.NumberFormat("en-AU", {
        style: "currency",
        currency: "AUD",
        minimumFractionDigits: 0,
    }).format(amount);
}

/**
 * Format date
 */
function formatDate(dateStr: string): string {
    return new Date(dateStr).toLocaleDateString("en-AU", {
        day: "numeric",
        month: "long",
        year: "numeric",
    });
}


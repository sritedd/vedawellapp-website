/**
 * Generate Defect List PDF content
 */
export function generateDefectListPDF(
    project: PDFReportData,
    defects: DefectReportItem[]
): string {
    const openDefects = defects.filter(d => d.status !== "verified");
    const criticalCount = defects.filter(d => d.severity === "critical").length;

    let content = `
═══════════════════════════════════════════════════════════════
                      DEFECT REPORT
═══════════════════════════════════════════════════════════════

PROJECT: ${project.projectName}
BUILDER: ${project.builderName}
DATE:    ${formatDate(project.generatedDate)}

SUMMARY
───────────────────────────────────────────────────────────────
Total Defects:      ${defects.length}
Open Defects:       ${openDefects.length}
Critical Items:     ${criticalCount}

DEFECT LIST
───────────────────────────────────────────────────────────────
`;

    defects.forEach((defect, index) => {
        content += `
${index + 1}. ${defect.title.toUpperCase()}
   Location:    ${defect.location}
   Severity:    ${defect.severity.toUpperCase()}
   Status:      ${defect.status}
   Reported:    ${formatDate(defect.reportedDate)}
   Description: ${defect.description}
`;
    });

    content += `
═══════════════════════════════════════════════════════════════
This report serves as formal documentation of outstanding defects.
Please address all items before proceeding with further payments.
═══════════════════════════════════════════════════════════════
Generated by HomeOwner Guardian
`;

    return content.trim();
}

/**
 * Generate Variation Report PDF content
 */
export function generateVariationReportPDF(
    project: PDFReportData,
    variations: VariationReportItem[]
): string {
    const totalValue = variations.reduce((sum, v) => sum + v.amount, 0);
    const variationPercentage = ((totalValue / project.contractValue) * 100).toFixed(1);

    let content = `
═══════════════════════════════════════════════════════════════
                    VARIATION REPORT
═══════════════════════════════════════════════════════════════

PROJECT: ${project.projectName}
BUILDER: ${project.builderName}
CONTRACT VALUE: ${formatCurrency(project.contractValue)}
DATE: ${formatDate(project.generatedDate)}

SUMMARY
───────────────────────────────────────────────────────────────
Total Variations:       ${variations.length}
Total Value:            ${formatCurrency(totalValue)}
% of Contract:          ${variationPercentage}%

${parseFloat(variationPercentage) > 10 ? "⚠️ WARNING: Variations exceed 10% of contract value" : ""}
${parseFloat(variationPercentage) > 15 ? "🚨 CRITICAL: Variations exceed 15% - review recommended" : ""}

VARIATION DETAILS
───────────────────────────────────────────────────────────────
`;

    variations.forEach((variation, index) => {
        content += `
${index + 1}. ${variation.title}
   Description: ${variation.description}
   Amount:      ${formatCurrency(variation.amount)}
   Status:      ${variation.status}
   Date:        ${formatDate(variation.date)}
`;
    });

    content += `
═══════════════════════════════════════════════════════════════
IMPORTANT: All variations should be agreed in writing before work
commences. Keep signed copies of all variation documents.
═══════════════════════════════════════════════════════════════
Generated by HomeOwner Guardian
`;

    return content.trim();
}

/**
 * Generate Payment Schedule PDF content
 */
export function generatePaymentSchedulePDF(
    project: PDFReportData,
    payments: PaymentMilestone[]
): string {
    const paidAmount = payments
        .filter(p => p.status === "paid")
        .reduce((sum, p) => sum + p.amount, 0);
    const remainingAmount = project.contractValue - paidAmount;

    let content = `
═══════════════════════════════════════════════════════════════
                   PAYMENT SCHEDULE
═══════════════════════════════════════════════════════════════

PROJECT: ${project.projectName}
BUILDER: ${project.builderName}
CONTRACT VALUE: ${formatCurrency(project.contractValue)}
DATE: ${formatDate(project.generatedDate)}

PAYMENT SUMMARY
───────────────────────────────────────────────────────────────
Total Contract:     ${formatCurrency(project.contractValue)}
Amount Paid:        ${formatCurrency(paidAmount)}
Remaining:          ${formatCurrency(remainingAmount)}

PAYMENT MILESTONES
───────────────────────────────────────────────────────────────
`;

    payments.forEach((payment) => {
        const statusIcon = payment.status === "paid" ? "✓" : payment.status === "due" ? "!" : " ";
        content += `
[${statusIcon}] ${payment.stage}
    Percentage:  ${payment.percentage}%
    Amount:      ${formatCurrency(payment.amount)}
    Status:      ${payment.status.toUpperCase()}
    Due Date:    ${payment.dueDate ? formatDate(payment.dueDate) : "TBD"}
`;
    });

    content += `
═══════════════════════════════════════════════════════════════
REMINDER: Always ensure required certificates are received
before releasing any progress payments.
═══════════════════════════════════════════════════════════════
Generated by HomeOwner Guardian
`;

    return content.trim();
}

